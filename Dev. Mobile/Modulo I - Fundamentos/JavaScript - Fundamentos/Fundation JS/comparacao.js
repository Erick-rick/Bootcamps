//Igual (==)
// Retorna true caso os operandos sejam iguais. 3 == var1
"3" == var1
3 == '3'

//Não igual(!=)
//Retorna verdadeiro caso os operando não sejma iguais var1 != 4
var2 != '3'

//Estritamente igual (===)
//Retorna verdadeiramente caso os operando sejam iguais e do mesmo tipo. Veja tbm Object.is e igualdade
3 === var1

//Estritamente não igual (!==)
//Retorna verdadeiramente caso os operando não sejam iguais e/ou não sejam do mesmo tipo.abs
3 !== '3'
var1 !== "3"

//Maior ou igual que (> / >=)
//Retorna verdadeiro caso o operando da esquerda seja maior ou igual que o da direita
var2 > var1
'12' > 2
var2 >= var1
var1 >= 3

//Menor ou igual que (< / <=)
//Retorna verdadeiro caso o operando da esquerda seja menor ou igual que o da direita
var2 < var1
'12' < 2
var2 <= var1
var1 <= 3

